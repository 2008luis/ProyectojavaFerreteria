    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
      Conexion con = new Conexion();
        if (jDatefechaFinal.getDate() == null || jDatefechaInicio.getDate() == null || txtEmpleado.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor selecciona una fecha de inicio y final y el nombre del empleado.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Date fechaInicio = new Date(jDatefechaInicio.getDate().getTime());
        Date fechaFinal = new Date(jDatefechaFinal.getDate().getTime());

        try (Connection connection = DriverManager.getConnection(con.getUrl(), con.getUser(), con.getPass())) {
            PreparedStatement stmt = connection.prepareCall("call ventasEmpleado(?,?,?)");
            stmt.setString(1, txtEmpleado.getText());
            stmt.setDate(2, fechaInicio);
            stmt.setDate(3, fechaFinal);

            ResultSet rs = stmt.executeQuery();
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("Vendedor");
            modelo.addColumn("Ventas");
            modelo.addColumn("Producto");
            modelo.addColumn("Precio");
            modelo.addColumn("Total Cantidad Vendida");
            modelo.addColumn("Total Recaudado");

            boolean empleadoEncontrado = false; 

            while (rs.next()) {
                empleadoEncontrado = true; 

                Object[] fila = new Object[6];
                fila[0] = rs.getString("Vendedor");
                fila[1] = rs.getInt("Ventas");
                fila[2] = rs.getString("Producto");
                fila[3] = rs.getDouble("Precio");
                fila[4] = rs.getInt("TotalCantidadVendida");
                fila[5] = rs.getDouble("TotalRecaudado");
                modelo.addRow(fila);
            }
            if (!empleadoEncontrado) {
                JOptionPane.showMessageDialog(null, "El empleado '" + txtEmpleado.getText() + "' no tiene ventas realizadas.", "Error", JOptionPane.ERROR_MESSAGE);
            }

            tableReportes.setModel(modelo);
        } catch (SQLException e) {
            e.printStackTrace();
            System.out.println(e.getMessage());
        }
       
        
    }//GEN-LAST:event_btnBuscarActionPerformed


